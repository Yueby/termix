export function getThemeById(id: string): TerminalTheme {
  const theme = terminalThemes.find((t) => t.id === id) ?? terminalThemes[0];
  const isDark = theme.variant === "dark";
  return {
    ...theme,
    colors: {
      scrollbarSliderBackground: isDark ? "rgba(255,255,255,0.15)" : "rgba(0,0,0,0.15)",
      scrollbarSliderHoverBackground: isDark ? "rgba(255,255,255,0.3)" : "rgba(0,0,0,0.3)",
      scrollbarSliderActiveBackground: isDark ? "rgba(255,255,255,0.4)" : "rgba(0,0,0,0.4)",
      ...theme.colors,
    },
  };
}
